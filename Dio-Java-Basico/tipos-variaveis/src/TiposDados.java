public class TiposDados {
    public static void main (String[] args){
        byte idade = 123;
        System.out.print(idade);
        short ano = 2021;
        System.out.print(ano);
        int cep = 21070333;
        System.out.print(cep);
        long cpf = 123123123L;//long precisa do L maiusculo, para representação, obrigatório
        System.out.print(cpf); 
        float pi = 3.14F; //float precisa do F maiusculo no final, obrigatorio
        System.out.print(pi);
        double salario = 1275.33;
        System.out.print(salario);
        final int NASCIMENTO = 1999;
        System.out.print(NASCIMENTO);
    }
   
}
